const myAddress = "0xaD55985619dE80191cfD7113658bE83099c1d80f";
const receiverAddress = "0x7DDaea79CDc22Ba9b4b873c67588587E380a8801";

async function interact() {
  const web3 = await new Web3("https://data-seed-prebsc-1-s1.binance.org:8545");
  const MFTContract = await new web3.eth.Contract(MFTAbi, MFTAddress);

  // call functions from contract: có 2 kiểu
  // call: chỉ để xem, TH này ko làm thay đổi dữ liệu của blockchain nên sẽ ko tốn gas
  // send: sẽ làm thay đổi dữ liệu của blockchain, sẽ phải trả phí gas và cần có private key để tương tác vs function đó

  const myBalance = await MFTContract.methods.balanceOf(myAddress).call();
  console.log(myBalance);

  await web3.eth.accounts.wallet.add(myPrivateKey);

  const receiverBalanceBefore = await MFTContract.methods
    .balanceOf(receiverAddress)
    .call();

  const rs = await MFTContract.methods
    .transfer(receiverAddress, 100000000)
    .send({ from: myAddress, gas: 3000000 });

  const receiverBalanceAfter = await MFTContract.methods
    .balanceOf(receiverAddress)
    .call();

  console.log(rs);
  console.log(receiverBalanceBefore);
  console.log(receiverBalanceAfter);
}
interact();
